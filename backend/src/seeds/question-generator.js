  function cite(t) {
    return `\n\n【出处】${t.date}《${t.source}》刊发《${t.article}》：\n"${t.excerpt}"`;
  }
  function withCite(analysis, t) {
    const base = typeof analysis === 'function' ? analysis(t) : analysis;
    return base + cite(t);
  }

